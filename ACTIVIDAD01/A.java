public class A {
    private String foo;
    private String bar;

    public A(String foo, String bar) {
        this.foo = foo;
        this.bar = bar;
    }

    // Getters y setters para foo y bar (opcional)
    public String getFoo() {
        return foo;
    }

    public void setFoo(String foo) {
        this.foo = foo;
    }

    public String getBar() {
        return bar;
    }

    public void setBar(String bar) {
        this.bar = bar;
    }
}
